var card1 = document.querySelectorAll("#one")
var heading1 = document.querySelectorAll("#one .content h1")
var icon1 = document.querySelectorAll("#one .icon i")

card1.forEach(function(hi){
    hi.addEventListener("mouseenter", function(){
        heading1.forEach(function(hello){
            hello.style.color = "#C80036"
        })
        icon1.forEach(function(i){
            i.style.color = "#C80036"
        })
    })
})

card1.forEach(function(hi){
    hi.addEventListener("mouseleave", function(){
        heading1.forEach(function(hello){
            hello.style.color = "#fff"
        })
        icon1.forEach(function(i){
            i.style.color = "#fff"
        })
    })
})

